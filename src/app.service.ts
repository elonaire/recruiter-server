import { Injectable } from '@nestjs/common';

@Injectable()
export class AppService {
  confirmGoogleOAuth(req) {
    
    if (!req.user) {
      return 'No user from google'
    }

    return {
      message: 'User information from google',
      user: req.user
    }
  }

  confirmFacebookOAuth(req) {
    
    if (!req.user) {
      return 'No user from google'
    }

    return {
      message: 'User information from google',
      user: req.user
    }
  }

  confirmLinkedInOAuth(req) {
    
    if (!req.user) {
      return 'No user from google'
    }

    return {
      message: 'User information from google',
      user: req.user
    }
  }
}
